class ArithmeticOperator
{
	public static void main(String args[])
	{
		int a=20;
		int b=10;
		System.out.print("Addition od a and b :");
		System.out.println(a+b);
		System.out.print("Substraction od a and b :");
		System.out.println(a-b);
		System.out.print("Multiplication od a and b :");
		System.out.println(a*b);
		System.out.print("Division od a and b :");
		System.out.println(a/b);
		System.out.print("Modulus od a and b :");
		System.out.println(a%b);
	}
}